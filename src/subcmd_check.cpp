  static
  void
  check_ggc_result(int rv_)
  {
    if(rv_ != GGC_OK)
      throw std::runtime_error("GGC codec failed");
  }

  static
  void
  write_word(void     *buf_,
             uint32_t  word_)
  {
    std::vector<uint32_t> *buf = (std::vector<uint32_t>*)buf_;

    buf->push_back(word_);
  }

  static
  void
  check_compression()
  {
    int rv;
    uint32_t *words;
    Compressor *comp;
    std::vector<uint32_t> local_compressed_data;

    rv = CreateCompressor(&comp,
                          (CompFunc)l::write_word,
                          NULL,
                          (void*)&local_compressed_data);
    if(rv < 0)
      throw std::runtime_error("CreateCompressor failed");

    words = (uint32_t*)uncompressed_data;
    for(unsigned i = 0; i < (uncompressed_data_len / sizeof(uint32_t)); ++i)
      FeedCompressor(comp,&words[i],1);

    rv = DeleteCompressor(comp);

    rv = memcmp(local_compressed_data.data(),compressed_data,compressed_data_len);

    if(rv == 0)
      fmt::print("* output of 3ct compressor matches SDK\n");
    else
      fmt::print("* output of 3ct compressor does NOT match SDK");
  }

  static
  void
  check_decompression()
  {
    int rv;
    uint32_t *words;
    Decompressor *decomp;
    std::vector<uint32_t> local_uncompressed_data;

    rv = CreateDecompressor(&decomp,
                            (CompFunc)l::write_word,
                            NULL,
                            (void*)&local_uncompressed_data);
    if(rv < 0)
      throw std::runtime_error("CreateCompressor failed");

    words = (uint32_t*)compressed_data;
    for(unsigned i = 0; i < (compressed_data_len / sizeof(uint32_t)); ++i)
      FeedDecompressor(decomp,&words[i],1);

    rv = DeleteDecompressor(decomp);

    rv = memcmp(local_uncompressed_data.data(),uncompressed_data,uncompressed_data_len);

    if(rv == 0)
      fmt::print("* output of 3ct decompressor matches SDK\n");
    else
      fmt::print("* output of 3ct decompressor does NOT match SDK");
  }

  static
  void
  check_ggc_decompression()
  {
    int rv;
    unsigned char *poed_data;
    size_t poed_data_len;

    poed_data = NULL;

    rv = ggc_decompress(poed_ggc_compressed,
                        poed_ggc_compressed_len,
                        &poed_data,
                        &poed_data_len);
    if(rv != GGC_OK)
      throw std::runtime_error("ggc_decompress failed");

    if(poed_data_len != poed_uncompressed_len)
      throw std::runtime_error("GGC decompression failed - mismatched length");

    rv = memcmp(poed_data,poed_uncompressed,poed_data_len);
    if(rv)
      throw std::runtime_error("GGC decompression failed - bytes don't match");

    ggc_free(poed_data);

    fmt::print("* output of 3ct GGC decompressor matches Game Guru\n");
  }

  static
  void
  check_ggc_compression()
  {
    int rv;
    unsigned char normal_file_type[4] = {' ',' ',' ',' '};
    unsigned char *poed_comp_data;
    size_t poed_comp_data_len;

    rv = ggc_compress(poed_uncompressed,
                      poed_uncompressed_len,
                      normal_file_type,
                      &poed_comp_data,
                      &poed_comp_data_len);
    check_ggc_result(rv);

    if(poed_comp_data_len != poed_ggc_compressed_len)
      throw std::runtime_error("GGC compression failed - mismatched length");

    rv = memcmp(poed_comp_data,poed_ggc_compressed,poed_comp_data_len);
    if(rv)
      throw std::runtime_error("GGC compression failed - bytes don't match");

    ggc_free(poed_comp_data);

    fmt::print("* output of 3ct GGC compressor matches Game Guru\n");
  }
